import React, { useState } from "react";
function SignInForm() {
  const users=[{email:"rachel@gmail.com",password:"rachelrachel"},{email:"person@gmail.com",password:"personperson"},{email:"yaelmitz@gmail.com",password:"yaelyael"}];
  const [state, setState] = React.useState({
    email: "",
    password: ""
  });
  const [isExist,setIsExist]=useState(false);
  const [isErrorEmail,setIsErrorEmail]=useState("");
  const [isErrorPass,setIsErrorPass]=useState("");
  
  const handleChange = evt => {
    const value = evt.target.value;
    setState({
      ...state,
      [evt.target.name]: value
    });
  };

  const handleOnSubmit = evt => {
    evt.preventDefault();
    const { email, password } = state;
    for (const key in state) {
      setState({
        ...state,
        [key]: ""
      });
    }
//  בדיקה האם קיים משתמש עם מייל כזה וסיסמא כזאת
  users.forEach(element => {
    if(element.email===email){
      if(element.password===password){
         setIsExist(true);
         alert(`You are login with email: ${email} and password: ${password}`);
         return;
      }
      else{
       //setIsErrorEmail("")
       setIsErrorPass("the password is wrong")
       return;
      }}
  });
  if(!isExist){
     setIsErrorPass("")
     setIsErrorEmail("the email is wrong")
  }
};

  return (
    <div className="form-container sign-in-container">
      <form onSubmit={handleOnSubmit}>
        <h1>Sign in</h1>
        <div className="social-container">
          <a href="#" className="social">
            <i className="fab fa-facebook-f" />
          </a>
          <a href="#" className="social">
            <i className="fab fa-google-plus-g" />
          </a>
          <a href="#" className="social">
            <i className="fab fa-linkedin-in" />
          </a>
        </div>
        <span>or use your account</span>
        <input
          type="email"
          placeholder="Email"
          name="email"
          value={state.email}
          onChange={handleChange}
        />
         {isErrorEmail}
        <input
          type="password"
          name="password"
          placeholder="Password"
          value={state.password}
          onChange={handleChange}
        />
        {isErrorPass}
        <a href="#">Forgot your password?</a>
        <button>Sign In</button>
      </form>
    </div>
  );
}

export default SignInForm;